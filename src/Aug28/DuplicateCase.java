public class DuplicateCase {
    public static void main(String[] args) {
    char c = 'B';

    switch (c){
        case 'A':
            System.out.println("A");
            break;
        case 'B':
            System.out.println("B");
            break;
        case 'C':
            System.out.println("65");
            break;
    }
}
}
